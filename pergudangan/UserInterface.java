    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        try {
            java.sql.Connection conn = (java.sql.Connection) pergudangan.Conn.ConnDB();
            if (conn == null) {
                JOptionPane.showMessageDialog(null, "Koneksi database gagal!");
                return;
            }

            // Validasi input field tidak kosong
            if (FieldBarang.getText().isEmpty() || KuantitasField.getText().isEmpty() || FieldIdBarang.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Harap isi semua field terlebih dahulu!");
                return;
            }

            String sql = "UPDATE Inventory_Data SET Barang=?, Kuantitas=? WHERE Id_Barang=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            // Mengatur nilai untuk parameter dengan urutan yang benar
            pst.setString(1, FieldBarang.getText());
            pst.setInt(2, Integer.parseInt(KuantitasField.getText()));  // Kuantitas dikonversi ke Integer
            pst.setString(3, FieldIdBarang.getText());

            // Menjalankan perintah update
            int rowsUpdated = pst.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil di Koreksi");
                showData();
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan atau gagal diupdate!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Kuantitas harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Proses Edit data Gagal: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

