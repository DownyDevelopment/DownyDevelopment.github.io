 public void showData() {
        DefaultTableModel tabel = new DefaultTableModel();
        tabel.addColumn("Id_Barang");
        tabel.addColumn("Barang");
        tabel.addColumn("Kuantitas");
        try {
            java.sql.Connection conn = (java.sql.Connection) pergudangan.Conn.ConnDB();
            String sql = "SELECT * FROM Inventory_Data";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            while (rs.next()) {
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3)});
            }
            jTable1.setModel(tabel);
        } catch (Exception e) {
        }
    }

    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        // TODO add your handling code here:
        try {
            java.sql.Connection conn = (java.sql.Connection) pergudangan.Conn.ConnDB();
            String sql = "SELECT * FROM Inventory_Data where Id_Barang='" + SearchField.getText() + "' or Barang='" + SearchField.getText() + "'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            if (rs.next()) {
                FieldIdBarang.setText(rs.getString(1));
                FieldBarang.setText(rs.getString(2));
                KuantitasField.setText(rs.getString(3));

                JOptionPane.showMessageDialog(null, "Data tbdata : "
                        + SearchField.getText() + " ditemukan, Apakah Anda Ingin Mengedit atau Menghapus?");
            }
        } catch (Exception e) {
        }
        SearchField.requestFocus();
    }//GEN-LAST:event_SearchButtonActionPerformed

